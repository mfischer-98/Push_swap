#include "push_swap.h"

void	push(t_stack **src, t_stack **dest)
{
	t_stack *temp;
	t_stack *src_node;

	src_node = *src;
	temp = *src;
	*src = temp->next;
	src_node->next = *dest;
	*dest = src_node;
}

void	pa(t_stack **a, t_stack **b)
{
	push(b, a);
	ft_printf("pa\n");
}

void	pb(t_stack **a, t_stack **b)
{
	push(a, b);
	ft_printf("pb\n");
}